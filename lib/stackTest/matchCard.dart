class MatchCard {
  int redColor = 0;
  int greenColor = 0;
  int blueColor = 0;
  double margin = 0;
  double marginL = 0;

  MatchCard(
      int red, int green, int blue, double marginBottom, double margeLeft) {
    redColor = red;
    greenColor = green;
    blueColor = blue;
    margin = marginBottom;
    marginL = margeLeft;
  }
}
